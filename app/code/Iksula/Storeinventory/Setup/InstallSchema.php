use Magento\Framework\DB\Ddl\Table;
use Magento\Framework\DB\Adapter\AdapterInterface;

class InstallSchema implements InstallSchemaInterface
{
    public function install(SchemaSetupInterface $setup, ModuleContextInterface $context)
    {
        $installer = $setup;

        $installer->startSetup();

        if (version_compare($context->getVersion(), '1.0.0') < 0){

		$installer->run('create table storeinventory_tb(id int not null auto_increment, sku varchar(255), store_id varchar(255) , original_price 	decimal(12,4), ecomm_price 	decimal(12,4) , buffer_inventory varchar(255) , inventory varchar(255) ,   primary key(id))');


		//demo 
